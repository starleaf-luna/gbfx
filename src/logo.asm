.pal_outloop:
	ld b, 3
	ld hl, hBGP
	ld de, WaterfallPaletteTable
.palloop:
	ld a, [de]
	ld [hl], a
	inc de
	ld c, 5
	call DelayFrames
	dec b
	jr z, .pal_outloop
	jr .palloop
	
